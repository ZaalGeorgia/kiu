package lesson20240917;

public class PrimitiveTypes {
	
	public static void main(String[] args) {
		
		byte b = 10;
		short s = 20000;
		char c = 'A';
		char c2 = 65;
		
		System.out.println(c);
		System.out.println(c2);
		
		int i = 1_000_000;
		
		long l = 10_000_000_000L;
		
		int m = 0xFF;
		int m2 = 077;
		
		System.out.println(m);
		System.out.println(m2);
		
	}

}
